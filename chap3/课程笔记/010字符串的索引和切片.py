s='HELLOWORLD'
print(s[0],s[-10]) #序号0和-10表示的是同一个字符
print(s[0],s[1],s[2],s[3],s[4],s[5],s[6],s[7],s[8],s[9])
print(s[-1],s[-2],s[-3],s[-4],s[-5],s[-6],s[-7],s[-8],s[-9],s[-10])
print('北京欢迎你'[4]) # 获取索引为4的字符
print('北京欢迎你'[-1])
print(s[2:7]) #切片:获取索引为2到7的字符
print(s[-8:-3]) #切片:获取索引为-8到-3的字符
print(s[:5]) # 默认从0开始
print(s[5:]) # 默认到最后
print(s[:]) # 默认从0开始，到最后
print('------------------')
print(s[::2]) # 步长为2
print(s[::-1]) # 逆序
print(s[::-2]) # 逆序，步长为2
print(s[2:7:2]) # 步长为2
print(s[-8:-3:2]) # 步长为2